    public void accionApagar(){
        try {
           if (webCheckBox.isSelected()) {
                servidorCompras.destroy();
            }
            if (infraCheckBox.isSelected()) {
                servidorInfracciones.destroy();
            }
            if (produCheckBox.isSelected()) {
                servidorProductos.destroy();
            }
            if (envioCheckBox.isSelected()) {
                servidorEnvios.destroy();
            }
            if (pagosCheckBox.isSelected()) {
                servidorPagos.destroy();
            }           
            }catch (Exception e) {
            Logger.getLogger(ConfAutomatico.class.getName()).log(Level.SEVERE, null, e);
        }
    }
