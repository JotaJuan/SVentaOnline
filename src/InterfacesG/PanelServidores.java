    public void iniciarServidores(){
        try {
            servidorProductos = Runtime.getRuntime().exec("java -jar jar/ServidorProductos.jar");
            servidorInfracciones = Runtime.getRuntime().exec("java -jar jar/ServidorInfracciones.jar");
            servidorEnvios = Runtime.getRuntime().exec("java -jar jar/ServidorEnvios.jar");            
            servidorPagos = Runtime.getRuntime().exec("java -jar jar/ServidorPagos.jar");
            servidorCompras = Runtime.getRuntime().exec("java -jar jar/ServidorCompras.jar");
        } catch (IOException ex) {
            Logger.getLogger(ConfAutomatico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void accionIniciar(){        
        try {
           if (webCheckBox.isSelected() && !servidorCompras.isAlive()) {
                servidorCompras = Runtime.getRuntime().exec("java -jar jar/ServidorCompras.jar");
            }
            if (infraCheckBox.isSelected()&& !servidorInfracciones.isAlive()) {
                servidorInfracciones = Runtime.getRuntime().exec("java -jar jar/ServidorInfracciones.jar");
            }
            if (produCheckBox.isSelected()&& !servidorProductos.isAlive()) {
                servidorProductos = Runtime.getRuntime().exec("java -jar jar/ServidorProductos.jar");
            }
            if (envioCheckBox.isSelected()&& !servidorEnvios.isAlive()) {
                servidorEnvios = Runtime.getRuntime().exec("java -jar jar/ServidorEnvios.jar");
            }
            if (pagosCheckBox.isSelected()&& !servidorPagos.isAlive()) {
                servidorPagos = Runtime.getRuntime().exec("java -jar jar/ServidorPagos.jar");
            }           
            }catch (Exception e) {
            Logger.getLogger(ConfAutomatico.class.getName()).log(Level.SEVERE, null, e);
        }
    }
